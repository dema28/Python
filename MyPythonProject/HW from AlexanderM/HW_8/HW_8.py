import json

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –∑–∞–≥—Ä—É–∑–∫–∏ –¥–∞–Ω–Ω—ã—Ö –∏–∑ —Ñ–∞–π–ª–∞
def load_flashcards(file_name="flashcards.json"):
    with open(file_name, "r", encoding="utf-8") as f:
        return json.load(f)

def flashcard_game(topic, flashcards):
    score = 0
    total_questions = len(flashcards[topic])
    incorrect_answers = []

    for word, translation in flashcards[topic].items():
        user_translation = input(f"–ö–∞–∫ –Ω–∞ —Ä—É—Å—Å–∫–æ–º –±—É–¥–µ—Ç {word}? ").strip().lower()

        if user_translation == translation:
            print("–í–µ—Ä–Ω–æ!")
            score += 1
        else:
            print(f"–ù–µ–≤–µ—Ä–Ω–æ! –ü—Ä–∞–≤–∏–ª—å–Ω—ã–π –ø–µ—Ä–µ–≤–æ–¥: {translation}")
            incorrect_answers.append((word, translation))

    print(f"–¢—ã –Ω–∞–±—Ä–∞–ª {score} –∏–∑ {total_questions}.")

    if incorrect_answers:
        print("–ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã–µ –æ—Ç–≤–µ—Ç—ã:")
        for word, translation in incorrect_answers:
            print(f"{word} - {translation}")


def main():
    flashcards = load_flashcards()  # –ó–∞–≥—Ä—É–∂–∞–µ–º —Å–ª–æ–≤–∞—Ä—å –∏–∑ —Ñ–∞–π–ª–∞

    while True:
        print(
            "–ü—Ä–∏–≤–µ—Ç! –í—ã–±–µ—Ä–∏ —Å–µ–≥–æ–¥–Ω—è—à–Ω—é—é —Ç–µ–º—É –¥–ª—è –∏–∑—É—á–µ–Ω–∏—è: city, fruits, animals, colors, professions, sports –∏–ª–∏ –≤–≤–µ–¥–∏ '–≤—ã–π—Ç–∏'.")
        topic = input().strip().lower()

        if topic == "–≤—ã–π—Ç–∏":
            print("–ù–æ —Ç—ã –ø—Ä–∏—Ö–æ–¥—å –µ—â–µ!")
            break
        elif topic in flashcards:
            flashcard_game(topic, flashcards)
        else:
            print("–ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π –≤—ã–±–æ—Ä, –ø–æ–ø—Ä–æ–±—É–π –µ—â–µ —Ä–∞–∑.")


if __name__ == "__main__":
    main()




